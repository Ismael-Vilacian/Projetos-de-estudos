    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        metodosPessoa acesso = new metodosPessoa();
        
        gravarDadosEvento acessoN = new gravarDadosEvento();
        boolean valorCarteira = acessoN.verificaFloat(jTextField5.getText());
        boolean valorGasto = acessoN.verificaFloat(jTextField6.getText());
        if(valorCarteira == true && valorGasto == true){
            try {
                String resultado = acesso.cadastroDePessoa(jTextField1.getText(),jTextField2.getText(),
                                        jTextField3.getText(),jTextField4.getText(),
                                        jTextField5.getText(),jTextField6.getText());
                JOptionPane.showMessageDialog(rootPane, resultado);
            } catch (IOException ex) {
                Logger.getLogger(cadastroPessoaInterface.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Dados incorretos, usuarios n√£o cadastrados!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


