    public void loadTabela() throws IOException{
        String csvArquivo = "dadosDeCompra.csv";
        BufferedReader conteudoCSV = null;
        String linha = "";
        String csvSeparadorCampo = ";";
        DefaultTableModel tabela = (DefaultTableModel) tabelaEvento.getModel();
        
            conteudoCSV = new BufferedReader(new FileReader(csvArquivo));
            //O loop continua ate reconhecer que n√£o tem mais linhas no arquivo
            while((linha = conteudoCSV.readLine()) != null){
                /*
                Pega os dados por linhaz e separa ao reconhecer ";"
                e grava os dados em um vetor com nome "evento"
                */
                String[] evento = linha.split(csvSeparadorCampo);
                //Imprime os dados dos eventos
                tabela.addRow(evento);
                
            }
            
        
        
    }
